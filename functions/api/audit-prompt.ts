// Format the user's 9 answers as a readable message for the model.
// Keep question texts in sync with src/lib/auditQuestions.ts.
export function buildUserMessage(answers: Array<string | { choice: string; other?: string }>): string {
  const questions = [
    "Уяви, що сьогодні ти отримала свій основний дохід. Що ти зазвичай робиш далі?",
    "Що ти робиш, коли несподівано отримуєш гроші? (подарунок, премія, бонус, додатковий дохід)",
    "Що ти зазвичай робиш, коли виникають несподівані витрати?",
    "Як ти зазвичай приймаєш рішення про великі покупки? Що допомагає зрозуміти, що зараз саме час купувати?",
    "Як ти вирішуєш, чи варто брати кредит або розстрочку? Що вирішальне?",
    "Чи вкладаєш ти зараз гроші у своє майбутнє? Чому обрала саме такий варіант / що зупиняє?",
    "Які почуття виникають у тебе наприкінці місяця, коли думаєш про фінанси? (варіант + опис)",
    "Яке фінансове рішення зараз для тебе найскладніше? Чому саме воно?",
    "Уяви, що минуло 5 років і твоє фінансове життя стало саме таким, як ти хотіла. Як зрозумієш, що це вже сталося? Що зробиш уперше?",
  ];

  const emotionChoiceLabel: Record<string, string> = {
    anxiety: "Тривога — ситуація стає гіршою",
    calm: "Спокій — загалом усе під контролем",
    confidence: "Задоволення — рухаюсь у правильному напрямку",
    other: "Інше",
  };

  const lines: string[] = ["Ось відповіді користувача на дев'ять питань діагностики:", ""];
  answers.forEach((a, i) => {
    lines.push(`### Питання ${i + 1}`);
    lines.push(questions[i]);
    lines.push("");
    lines.push("Відповідь:");
    if (typeof a === "string") {
      lines.push(a.trim() || "(без відповіді)");
    } else {
      const label = emotionChoiceLabel[a.choice] ?? a.choice;
      lines.push(`Обраний варіант: ${label}`);
      if (a.other && a.other.trim()) {
        lines.push(`Пояснення своїми словами: ${a.other.trim()}`);
      }
    }
    lines.push("");
  });
  lines.push("Проаналізуй ці відповіді відповідно до інструкцій у system prompt.");
  return lines.join("\n");
}

